@Mod(ExampleMod.MODID)
public class ExampleMod
{
    public static final String MODID = "jay_mod";
    private static final Logger LOGGER = LogUtils.getLogger();
    public static final DeferredRegister<Block> BLOCKS = DeferredRegister.create(ForgeRegistries.BLOCKS, MODID);
    public static final DeferredRegister<Item> ITEMS = DeferredRegister.create(ForgeRegistries.ITEMS, MODID);
    public static final DeferredRegister<Feature<?>> FEATURES = DeferredRegister.create(ForgeRegistries.FEATURES, MODID);

    public static final RegistryObject<Block> EXAMPLE_BLOCK = BLOCKS.register("example_block", () -> new Block(BlockBehaviour.Properties.of(Material.STONE)));
    public static final RegistryObject<Item> EXAMPLE_BLOCK_ITEM = ITEMS.register("example_block", () -> new BlockItem(EXAMPLE_BLOCK.get(), new Item.Properties().tab(CreativeModeTab.TAB_BUILDING_BLOCKS)));
    public static final RegistryObject<Feature<NoneFeatureConfiguration>> BAD_BLOCK_FEATURE = FEATURES.register("bad_block", () -> new BadBlockFeature(NoneFeatureConfiguration.CODEC));

    public ExampleMod()
    {
        IEventBus modEventBus = FMLJavaModLoadingContext.get().getModEventBus();
        FEATURES.register(modEventBus);
        BLOCKS.register(modEventBus);
        ITEMS.register(modEventBus);
        modEventBus.addListener(this::commonSetup);
        MinecraftForge.EVENT_BUS.register(this);
        ModLoadingContext.get().registerConfig(ModConfig.Type.COMMON, Config.SPEC);
    }

    private void commonSetup(final FMLCommonSetupEvent event)
    {
        LOGGER.info("HELLO FROM COMMON SETUP");
        LOGGER.info("BAD_BLOCK_FEATURE >> {}", BAD_BLOCK_FEATURE.getId());
    }

    @SubscribeEvent
    public void onServerStarting(ServerStartingEvent event)
    {
        LOGGER.info("HELLO from server starting");
    }

    @Mod.EventBusSubscriber(modid = MODID, bus = Mod.EventBusSubscriber.Bus.MOD, value = Dist.CLIENT)
    public static class ClientModEvents
    {
        @SubscribeEvent
        public static void onClientSetup(FMLClientSetupEvent event)
        {
            LOGGER.info("HELLO FROM CLIENT SETUP");
            LOGGER.info("MINECRAFT NAME >> {}", Minecraft.getInstance().getUser().getName());
        }
    }

    @Mod.EventBusSubscriber(modid = MODID, value = Dist.CLIENT)
    public static class ModEventSubscriber {
        @SubscribeEvent
        public static void onMobSpawn(LivingSpawnEvent.CheckSpawn event) {
            LivingEntity e = event.getEntity();
            if (e instanceof Creeper || e instanceof Skeleton) {
                event.setResult(Event.Result.DENY);
                if (event.getLevel() instanceof ServerLevel serverLevel) {
                    LivingEntity enderman = new EnderMan(EntityType.ENDERMAN, serverLevel);
                    enderman.moveTo(event.getX(), event.getY(), event.getZ(), 0.0F, 0.0F);
                    serverLevel.addFreshEntity(enderman);
                }
            }
        }
    }
}
